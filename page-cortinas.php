	/*****************/
	
	class GallerySlider extends HTMLElement{
		constructor(){
			super();
			this.images = this.querySelectorAll('.img-wrapper > img');
			this.modal = this.querySelector('#modal-slider');
			this.close = this.querySelector('#close-modal');

			this.handleEvents();	
		}

		handleEvents(){
			this.images.forEach(image => image.addEventListener('click', this.handleClick.bind(this)));	
			this.close.addEventListener('click', this.closeModal.bind(this));
		}

		handleClick(e){
			let index = Number(e.target.dataset.image);
			this.modal.open = true;
			// console.log(gallleryModal)
		}

		closeModal(){
			this.modal.removeAttribute('open');
		}
	}

	window.customElements.define('gallery-slider', GallerySlider);
</script>


